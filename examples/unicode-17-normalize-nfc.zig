// Normalize UTF-8 input to NFC. Returns 0 on invalid/overflow.

const std = @import("std");

const INPUT_CAP: usize = 1024 * 1024;
const OUTPUT_CAP: usize = INPUT_CAP * 4;
const MAX_CODEPOINTS: usize = INPUT_CAP * 2;

var input_buf: [INPUT_CAP]u8 = undefined;
var output_buf: [OUTPUT_CAP]u8 = undefined;
var tmp_a: [MAX_CODEPOINTS]u32 = undefined;
var tmp_b: [MAX_CODEPOINTS]u32 = undefined;

export fn input_ptr() u32 {
    return @as(u32, @intCast(@intFromPtr(&input_buf)));
}

export fn input_utf8_cap() u32 {
    return @as(u32, @intCast(INPUT_CAP));
}

export fn output_ptr() u32 {
    return @as(u32, @intCast(@intFromPtr(&output_buf)));
}

export fn output_utf8_cap() u32 {
    return @as(u32, @intCast(OUTPUT_CAP));
}

fn isContinuation(b: u8) bool {
    return (b & 0xC0) == 0x80;
}

fn decodeUtf8(input: []const u8, idx: *usize) ?u32 {
    if (idx.* >= input.len) return null;
    const b0 = input[idx.*];
    if (b0 <= 0x7F) {
        idx.* += 1;
        return b0;
    }
    if (b0 >= 0xC2 and b0 <= 0xDF) {
        if (idx.* + 1 >= input.len) @trap();
        const b1 = input[idx.* + 1];
        if (!isContinuation(b1)) @trap();
        idx.* += 2;
        return (@as(u32, b0 & 0x1F) << 6) | (@as(u32, b1 & 0x3F));
    }
    if (b0 >= 0xE0 and b0 <= 0xEF) {
        if (idx.* + 2 >= input.len) @trap();
        const b1 = input[idx.* + 1];
        const b2 = input[idx.* + 2];
        if (!isContinuation(b1) or !isContinuation(b2)) @trap();
        if (b0 == 0xE0 and b1 < 0xA0) @trap();
        if (b0 == 0xED and b1 >= 0xA0) @trap();
        idx.* += 3;
        return (@as(u32, b0 & 0x0F) << 12) | (@as(u32, b1 & 0x3F) << 6) | (@as(u32, b2 & 0x3F));
    }
    if (b0 >= 0xF0 and b0 <= 0xF4) {
        if (idx.* + 3 >= input.len) @trap();
        const b1 = input[idx.* + 1];
        const b2 = input[idx.* + 2];
        const b3 = input[idx.* + 3];
        if (!isContinuation(b1) or !isContinuation(b2) or !isContinuation(b3)) @trap();
        if (b0 == 0xF0 and b1 < 0x90) @trap();
        if (b0 == 0xF4 and b1 >= 0x90) @trap();
        idx.* += 4;
        return (@as(u32, b0 & 0x07) << 18) | (@as(u32, b1 & 0x3F) << 12) | (@as(u32, b2 & 0x3F) << 6) | (@as(u32, b3 & 0x3F));
    }
    @trap();
}

fn findIndex(list: []const u32, key: u32) ?usize {
    var lo: usize = 0;
    var hi: usize = list.len;
    while (lo < hi) {
        const mid = (lo + hi) / 2;
        const v = list[mid];
        if (v == key) return mid;
        if (v < key) {
            lo = mid + 1;
        } else {
            hi = mid;
        }
    }
    return null;
}

fn getCCC(cp: u32) u8 {
    if (findIndex(ccc_codepoints, cp)) |idx| {
        return ccc_values[idx];
    }
    return 0;
}

const SBase: u32 = 0xAC00;
const LBase: u32 = 0x1100;
const VBase: u32 = 0x1161;
const TBase: u32 = 0x11A7;
const LCount: u32 = 19;
const VCount: u32 = 21;
const TCount: u32 = 28;
const NCount: u32 = VCount * TCount;
const SCount: u32 = LCount * NCount;

fn isHangulSyllable(cp: u32) bool {
    return cp >= SBase and cp < SBase + SCount;
}

fn decomposeHangul(cp: u32, out: []u32, out_len: *usize) bool {
    const s_index = cp - SBase;
    const l_index = s_index / NCount;
    const v_index = (s_index % NCount) / TCount;
    const t_index = s_index % TCount;
    if (!appendCp(out, out_len, LBase + l_index)) @trap();
    if (!appendCp(out, out_len, VBase + v_index)) @trap();
    if (t_index != 0) {
        if (!appendCp(out, out_len, TBase + t_index)) @trap();
    }
    return true;
}

fn appendCp(out: []u32, out_len: *usize, cp: u32) bool {
    if (out_len.* >= out.len) @trap();
    out[out_len.*] = cp;
    out_len.* += 1;
    return true;
}

fn decompose(cp: u32, out: []u32, out_len: *usize) bool {
    if (isHangulSyllable(cp)) {
        return decomposeHangul(cp, out, out_len);
    }
    if (findIndex(decomp_codepoints, cp)) |idx| {
        const start = decomp_index[idx];
        const len = decomp_lens[idx];
        var i: u32 = 0;
        while (i < len) : (i += 1) {
            const dcp = decomp_data[@intCast(start + i)];
            if (!decompose(dcp, out, out_len)) @trap();
        }
        return true;
    }
    return appendCp(out, out_len, cp);
}

fn reorderCanonical(buf: []u32, len: usize) void {
    var i: usize = 0;
    while (i < len) {
        const seg_start = i;
        i += 1;
        while (i < len and getCCC(buf[i]) != 0) : (i += 1) {}
        const seg_end = i;
        var j = seg_start + 1;
        while (j < seg_end) : (j += 1) {
            const cp = buf[j];
            const ccc = getCCC(cp);
            var k = j;
            while (k > seg_start and getCCC(buf[k - 1]) > ccc) {
                buf[k] = buf[k - 1];
                k -= 1;
            }
            buf[k] = cp;
        }
    }
}

fn isL(cp: u32) bool {
    return cp >= LBase and cp < LBase + LCount;
}

fn isV(cp: u32) bool {
    return cp >= VBase and cp < VBase + VCount;
}

fn isT(cp: u32) bool {
    return cp > TBase and cp < TBase + TCount;
}

fn isLV(cp: u32) bool {
    return isHangulSyllable(cp) and ((cp - SBase) % TCount == 0);
}

fn composePair(starter: u32, comb: u32) ?u32 {
    if (isL(starter) and isV(comb)) {
        const l_index = starter - LBase;
        const v_index = comb - VBase;
        return SBase + (l_index * VCount + v_index) * TCount;
    }
    if (isLV(starter) and isT(comb)) {
        return starter + (comb - TBase);
    }

    if (findIndex(comp_starters, starter)) |idx| {
        const start = comp_index[idx];
        const end = if (idx + 1 < comp_index.len) comp_index[idx + 1] else comp_combining.len;
        var lo: usize = @intCast(start);
        var hi: usize = @intCast(end);
        while (lo < hi) {
            const mid = (lo + hi) / 2;
            const v = comp_combining[mid];
            if (v == comb) return comp_composite[mid];
            if (v < comb) {
                lo = mid + 1;
            } else {
                hi = mid;
            }
        }
    }
    return null;
}

fn composeNFC(input: []const u32, len: usize, out: []u32) ?usize {
    if (len == 0) return 0;
    var out_len: usize = 0;
    var starter: u32 = 0;
    var starter_index: usize = 0;
    var starter_valid = false;
    var last_ccc: u8 = 0;

    var i: usize = 0;
    while (i < len) : (i += 1) {
        const cp = input[i];
        const ccc = getCCC(cp);
        if (out_len == 0) {
            if (!appendCp(out, &out_len, cp)) @trap();
            starter = cp;
            starter_index = 0;
            starter_valid = (ccc == 0);
            last_ccc = if (ccc == 0) 0 else ccc;
            continue;
        }

        if (starter_valid and (isL(starter) and isV(cp))) {
            const comp = composePair(starter, cp) orelse @trap();
            out[starter_index] = comp;
            starter = comp;
            last_ccc = 0;
            continue;
        }
        if (starter_valid and (isLV(starter) and isT(cp))) {
            const comp = composePair(starter, cp) orelse @trap();
            out[starter_index] = comp;
            starter = comp;
            last_ccc = 0;
            continue;
        }

        if (ccc == 0) {
            if (!appendCp(out, &out_len, cp)) @trap();
            starter = cp;
            starter_index = out_len - 1;
            starter_valid = true;
            last_ccc = 0;
            continue;
        }

        if (starter_valid and last_ccc < ccc) {
            if (composePair(starter, cp)) |comp| {
                out[starter_index] = comp;
                starter = comp;
                continue;
            }
        }

        if (!appendCp(out, &out_len, cp)) @trap();
        last_ccc = ccc;
    }

    return out_len;
}

fn writeUtf8(cp: u32, out: []u8, out_len: *usize) bool {
    if (cp <= 0x7F) {
        if (out_len.* + 1 > out.len) return false;
        out[out_len.*] = @intCast(cp);
        out_len.* += 1;
        return true;
    }
    if (cp <= 0x7FF) {
        if (out_len.* + 2 > out.len) return false;
        out[out_len.*] = @intCast(0xC0 | (cp >> 6));
        out[out_len.* + 1] = @intCast(0x80 | (cp & 0x3F));
        out_len.* += 2;
        return true;
    }
    if (cp <= 0xFFFF) {
        if (cp >= 0xD800 and cp <= 0xDFFF) return false;
        if (out_len.* + 3 > out.len) return false;
        out[out_len.*] = @intCast(0xE0 | (cp >> 12));
        out[out_len.* + 1] = @intCast(0x80 | ((cp >> 6) & 0x3F));
        out[out_len.* + 2] = @intCast(0x80 | (cp & 0x3F));
        out_len.* += 3;
        return true;
    }
    if (cp <= 0x10FFFF) {
        if (out_len.* + 4 > out.len) return false;
        out[out_len.*] = @intCast(0xF0 | (cp >> 18));
        out[out_len.* + 1] = @intCast(0x80 | ((cp >> 12) & 0x3F));
        out[out_len.* + 2] = @intCast(0x80 | ((cp >> 6) & 0x3F));
        out[out_len.* + 3] = @intCast(0x80 | (cp & 0x3F));
        out_len.* += 4;
        return true;
    }
    return false;
}

export fn run(input_size_in: u32) u32 {
    var input_size: usize = input_size_in;
    if (input_size > INPUT_CAP) input_size = INPUT_CAP;
    const input = input_buf[0..input_size];

    var decomp_len: usize = 0;
    var idx: usize = 0;
    while (idx < input.len) {
        const cp = decodeUtf8(input, &idx).?;
        if (!decompose(cp, tmp_a[0..], &decomp_len)) return 0;
    }

    reorderCanonical(tmp_a[0..decomp_len], decomp_len);

    const comp_len = composeNFC(tmp_a[0..decomp_len], decomp_len, tmp_b[0..]) orelse return 0;

    var out_len: usize = 0;
    var i: usize = 0;
    while (i < comp_len) : (i += 1) {
        if (!writeUtf8(tmp_b[0..comp_len][i], output_buf[0..], &out_len)) @trap();
    }

    return @as(u32, @intCast(out_len));
}

fn normalizeForTest(input: []const u8) []const u8 {
    if (input.len > INPUT_CAP) @trap();
    @memcpy(input_buf[0..input.len], input);
    const out_len = run(@as(u32, @intCast(input.len)));
    return output_buf[0..@as(usize, @intCast(out_len))];
}

// https://www.unicode.org/Public/17.0.0/ucd/NormalizationTest.txt
test "nfc normalizes combining sequence" {
    const input = "e\u{0301}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{00E9}", output);
}

test "nfc composes hangul" {
    const input = "\u{1100}\u{1161}\u{11A8}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{AC01}", output);
}

test "nfc leaves ascii unchanged" {
    const input = "hello";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings(input, output);
}

test "nfc reorders combining marks and composes" {
    const input = "D\u{0307}\u{0323}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{1E0C}\u{0307}", output);
}

test "nfc composes sequential combining marks" {
    const input = "E\u{0304}\u{0300}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{1E14}", output);
}

test "nfc composes hangul with existing syllable" {
    const input = "\u{1100}\u{AC00}\u{11A8}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{1100}\u{AC01}", output);
}

test "nfc reorders hebrew combining marks" {
    const input = "\u{05B8}\u{05B9}\u{05B1}\u{0591}\u{05C3}\u{05B0}\u{05AC}\u{059F}";
    const output = normalizeForTest(input);
    try std.testing.expectEqualStrings("\u{05B1}\u{05B8}\u{05B9}\u{0591}\u{05C3}\u{05B0}\u{05AC}\u{059F}", output);
}

