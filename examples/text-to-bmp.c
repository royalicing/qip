static uint32_t count_rows(uint32_t input_size) {
    uint32_t rows = 1;
    uint32_t col = 0;
    uint32_t i = 0;
    while (i < input_size) {
        unsigned char c = input_buffer[i];
        if (c == '\r') {
            if (i + 1 < input_size && input_buffer[i + 1] == '\n') {
                i++;
            }
            rows++;
            col = 0;
            i++;
            continue;
        }
        if (c == '\n') {
            rows++;
            col = 0;
            i++;
            continue;
        }
        if (c == '\t') {
            uint32_t spaces = 4 - (col % 4);
            col += spaces;
            while (col >= COLS) {
                rows++;
                col = 0;
            }
            i++;
            continue;
        }
        col++;
        if (col >= COLS) {
            rows++;
            col = 0;
        }
        i++;
    }
    return rows;
}

__attribute__((export_name("run")))
uint32_t run(uint32_t input_size) {
    if (input_size > INPUT_CAP) {
        input_size = INPUT_CAP;
    }

    uint32_t rows = count_rows(input_size);
    uint32_t row_h = GLYPH_H + (uint32_t)leading_px;
    uint32_t width = COLS * GLYPH_W;
    uint32_t height = rows * row_h;
    uint64_t pixel_bytes = (uint64_t)width * (uint64_t)height * 4u;
    uint64_t total = 54u + pixel_bytes;
    if (total > OUTPUT_CAP) {
        return 0;
    }

    // Fill background white
    for (uint64_t i = 0; i < total; i++) {
        output_buffer[i] = 0xFF;
    }

    // BMP header
    output_buffer[0] = 'B';
    output_buffer[1] = 'M';
    write_u32_le(2, (uint32_t)total);
    write_u32_le(6, 0);
    write_u32_le(10, 54);
    write_u32_le(14, 40);
    write_u32_le(18, width);
    write_u32_le(22, height);
    write_u16_le(26, 1);
    write_u16_le(28, 32);
    write_u32_le(30, 0);
    write_u32_le(34, (uint32_t)pixel_bytes);
    write_u32_le(38, 2835);
    write_u32_le(42, 2835);
    write_u32_le(46, 0);
    write_u32_le(50, 0);

    uint32_t col = 0;
    uint32_t row = 0;
    uint32_t i = 0;
    while (i < input_size && row < rows) {
        unsigned char c = input_buffer[i];
        if (c == '\r') {
            if (i + 1 < input_size && input_buffer[i + 1] == '\n') {
                i++;
            }
            row++;
            col = 0;
            i++;
            continue;
        }
        if (c == '\n') {
            row++;
            col = 0;
            i++;
            continue;
        }
        if (c == '\t') {
            uint32_t spaces = 4 - (col % 4);
            for (uint32_t s = 0; s < spaces; s++) {
                col++;
                if (col >= COLS) {
                    row++;
                    col = 0;
                    if (row >= rows) break;
                }
            }
            i++;
            continue;
        }

        unsigned char glyph = c;
        if (glyph < 32 || glyph >= 128) {
            glyph = '?';
        }

        uint32_t base_x = col * GLYPH_W;
        uint32_t base_y = row * row_h;
        for (uint32_t gy = 0; gy < GLYPH_H; gy++) {
            unsigned char bits = font8x8_basic[glyph][gy];
            for (uint32_t gx = 0; gx < GLYPH_W; gx++) {
                if (bits & (1u << (7u - gx))) {
                    set_pixel(width, height, base_x + gx, base_y + gy, 0, 0, 0, 255);
                }
            }
        }

        col++;
        if (col >= COLS) {
            row++;
            col = 0;
        }
        i++;
    }

    return (uint32_t)total;
}
